// Next bigger number with the same digits -------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
var results = [];
function permute(arr, memo) {
  var cur;
  var memo = memo || [];

  for (var i = 0; i < arr.length; i++) {
    cur = arr.splice(i, 1);

    if (arr.length === 0) {
      results.push(+memo.concat(cur).join(''));
    }

    permute(arr.slice(), memo.concat(cur));
    arr.splice(i, 0, cur[0]);
  }
  return results;
}

const nextBigger = n =>
  permute((n + '').split(''))
    .sort((a, b) => a - b)
    .find(el => el > n) || -1;

